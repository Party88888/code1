package Week11;
public class WhaleLoop_01 {
    public static void main(String[] args) {
        int count = 1;   // ค่าเริ่มต้น
        while (count <= 100) {   // เงื่อนไข
            System.out.println("รอบที่"+count+"Hello");   // ค่าที่ต้องการผล
          count++; // update รอบ
            
        } 
    }
}
